    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        try{
            Cliente cli = new Cliente();
            Pet pet = new Pet();
            Insert in = new Insert();
        
            cli.setCpf(jTextCadCPF.getText());
            cli.setNome(jTextCadNome.getText());
            cli.setTelefone(jTextCadTelefone.getText());
            pet.setCpfResponsavel(jTextCadCPF.getText());
            pet.setNome(jTextCadNomePet.getText());
            pet.setRaca(jTextCadRaca.getText());
            
            
            jMensagem.setText(in.cadastraCliente(cli));
            jMensagem1.setText(in.cadastraPet(pet));
        }catch (Exception e){
            jMensagem.setText("Erro ao cadastrar!!!");
        }
        
        
        
        
        
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

