    private void jButtonExecutarTarefaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecutarTarefaActionPerformed
        // TODO add your handling code here:
        Pet pet = new Pet();
        Update cotrollerPets = new Update();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        pet.setCpfResponsavel(model.getValueAt(jTable1.getSelectedRow(),0).toString());
        pet.setRaca(model.getValueAt(jTable1.getSelectedRow(),1).toString());
        pet.setNome(model.getValueAt(jTable1.getSelectedRow(),2).toString());
        pet.setHistCuida("{" +jLabelUsuario.getText() +" Serviço executando: " + jTextTarefaExecutada.getText());
        //model.setValueAt(pet.getHistCuida(), jTable1.getSelectedRow(), 4);
        cotrollerPets.alterarPet(pet);
        
        listar();
           
        //jTable1.clearSelection();
        
    }//GEN-LAST:event_jButtonExecutarTarefaActionPerformed
    private void listar(){
        Select cotrollerPets = new Select();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int x = model.getRowCount();
        for(int a = 0; a < x; a++){
            model.removeRow(0);
        }
        for(Pet pet : cotrollerPets.listaPet()){
            model.addRow(new Object[]{pet.getCpfResponsavel(), pet.getRaca(), pet.getNome(), pet.getHistCuida()});
        }
           
    }
